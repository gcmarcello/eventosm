export const dateRegex =
  /^(0[1-9]|[12][0-9]|3[01])\/(0[1-9]|1[012])\/(19|20)\d\d$/; // DD/MM/YYYY
export const dateTimeRegex =
  /^(0[1-9]|[12][0-9]|3[01])\/(0[1-9]|1[0-2])\/\d{4} ([01][0-9]|2[0-3]):[0-5][0-9]$/; // DD/MM/YYYY HH:mm
export const timeRegex = /^(0[0-9]|1[0-9]|2[0-3]):([0-5][0-9])$/; // HH:mm
