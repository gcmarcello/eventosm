export type Result = {
  POS: string;
  NUM: string;
  ATLETA: string;
  EQUIPE: string;
  CATEGORIA: string;
  TEMPO: string;
  RITMO: string;
};

export default function EventGroupResultsPage() {
  const [calculatedResults, setCalculatedResults] = useState<Result[]>([]);
  const handlePlaces = (position: number) => {
    switch (position) {
      case 1:
        return (
          <div className="mx-auto flex size-6 items-center justify-center rounded-full bg-yellow-500 font-semibold text-black shadow-md">
            1
          </div>
        );
      case 2:
        return (
          <div className="mx-auto flex size-6 items-center justify-center rounded-full bg-gray-500 font-semibold text-white shadow-md">
            2
          </div>
        );
      case 3:
        return (
          <div className="mx-auto flex size-6 items-center justify-center rounded-full bg-yellow-700 font-semibold text-white shadow-md">
            3
          </div>
        );
      case 4:
        return (
          <div className="mx-auto flex size-6 items-center justify-center rounded-full bg-blue-700 font-semibold text-white shadow-md">
            4
          </div>
        );
      case 5:
        return (
          <div className="mx-auto flex size-6 items-center justify-center rounded-full bg-blue-500 font-semibold text-white shadow-md">
            5
          </div>
        );

      default:
        return (
          <div className="semibold mx-auto flex size-6 items-center justify-center rounded-full ">
            {position}
          </div>
        );
    }
  };
  const uniqueCategories: string[] = useMemo(
    () => Array.from(new Set(results.map((obj) => obj.CATEGORIA))).sort(),
    []
  );
  const uniqueTeams: string[] = useMemo(
    () =>
      Array.from(
        new Set(results.filter((r) => r.EQUIPE).map((obj) => obj.EQUIPE))
      ).sort(),
    []
  );

  const form = useForm({
    schema: z.object({
      CATEGORIA: z.string().optional(),
      EQUIPE: z.string().optional(),
    }),
  });

  function timeToSeconds(timeStr: string): number | "DNF" {
    const [hours, minutes, seconds] = timeStr.split(":").map(Number);
    if (!hours || !minutes || !seconds) return "DNF";
    return hours * 3600 + minutes * 60 + seconds;
  }

  const Field = useMemo(() => form.createField(), []);

  function sortPositions(resultArray: Result[]) {
    const sortedArray = resultArray.sort((a, b) => {
      const aTime = timeToSeconds(a.TEMPO);
      const bTime = timeToSeconds(b.TEMPO);
      if (aTime === "DNF" || bTime === "DNF") return 1;
      return aTime - bTime;
    });
    sortedArray.forEach((obj, index) => {
      obj.POS = String(index + 1);
    });
    return sortedArray;
  }

  useEffect(() => {
    setCalculatedResults(sortPositions(results));
  }, []);

  function handleFilter(type: "CATEGORIA" | "EQUIPE", value: any) {
    if (!value) return setCalculatedResults(sortPositions(results));
    setCalculatedResults(
      sortPositions(results.filter((obj) => obj[type] === value))
    );
  }

  function clearFilter() {
    form.reset({ CATEGORIA: "", EQUIPE: "" });
    setCalculatedResults(sortPositions(results));
  }

  return (
    <div className="mt-8 px-4 lg:px-32">
      <Title>Classificação Geral</Title>
      {calculatedResults.length ? (
        <>
          <Form hform={form} className="mt-4 items-center gap-3">
            <Fieldset>
              <FieldGroup className="flex-row space-y-3 lg:flex lg:items-end lg:justify-between lg:gap-2 lg:space-y-0">
                <div className="gap-2 lg:flex">
                  <Field name="CATEGORIA">
                    <Label>Filtrar por Categoria</Label>
                    <Select
                      disabled={Boolean(form.watch("EQUIPE"))}
                      data={uniqueCategories.map((c) => ({ id: c }))}
                      displayValueKey="id"
                      onChange={(event: any) =>
                        handleFilter("CATEGORIA", event.target.value)
                      }
                    />
                  </Field>
                  <Field name="EQUIPE">
                    <Label>Filtrar por Equipe</Label>
                    <Select
                      disabled={Boolean(form.watch("CATEGORIA"))}
                      data={uniqueTeams.map((t) => ({ id: t }))}
                      displayValueKey="id"
                      onChange={(event: any) =>
                        handleFilter("EQUIPE", event.target.value)
                      }
                    />
                  </Field>
                </div>
                <div className="pb-1">
                  <Button
                    className={"my-auto  w-full lg:w-auto"}
                    onClick={() => clearFilter()}
                  >
                    Limpar Filtros
                  </Button>
                </div>
              </FieldGroup>
            </Fieldset>
          </Form>
          <Table
            data={calculatedResults}
            columns={(columnHelper) => [
              columnHelper.accessor("POS", {
                id: "position",
                header: "Posição",
                enableSorting: true,
                enableGlobalFilter: true,
                cell: (info) => handlePlaces(Number(info.getValue())),
              }),
              columnHelper.accessor("NUM", {
                id: "number",
                header: "Numero",
                enableSorting: true,
                enableGlobalFilter: true,
                cell: (info) => Number(info.getValue()),
              }),
              columnHelper.accessor("ATLETA", {
                id: "name",
                header: "Nome",
                enableSorting: true,
                enableGlobalFilter: true,
                cell: (info) => info.getValue(),
              }),
              columnHelper.accessor("CATEGORIA", {
                id: "category",
                header: "Categoria",
                enableSorting: true,
                enableGlobalFilter: true,
                cell: (info) => info.getValue(),
              }),
              columnHelper.accessor("EQUIPE", {
                id: "team",
                header: "Equipe",
                enableSorting: true,
                enableGlobalFilter: true,
                cell: (info) => info.getValue(),
              }),

              columnHelper.accessor("TEMPO", {
                id: "time",
                header: "Tempo",
                enableSorting: true,
                enableGlobalFilter: true,
                cell: (info) => info.getValue(),
              }),
            ]}
          />
        </>
      ) : (
        <LoadingSpinner />
      )}
    </div>
  );
}
